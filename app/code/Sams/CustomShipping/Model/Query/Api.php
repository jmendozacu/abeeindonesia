    public function apiCaller($url, $method, $params, $header = null)
    {
        $apiCaller = $this->_httpClient->create();
        $apiCaller->setUri($url);
        $apiCaller->setMethod($method);
        $apiCaller->setHeaders([
            'Content-Type: application/x-www-form-urlencoded',
            'Accept: application/json',
            'Key: ' . $this->key,
        ]);

        $apiCaller->setParameterPost($params); //or parameter get
        return $apiCaller->request();
    }

    public function getOngkir($origin, $destination, $weight, $courier)
    {
        $params = [
            'origin' => '331',
            'originType' => 'city',
            'destination' => '331',
            'destinationType' => 'city',
            'weight' => 100,
            'courier' => 'jne'
        ];


        $ongkir = $this->apiCaller('https://pro.rajaongkir.com/api/cost', \Zend_Http_Client::POST, $params, $this->key);

        if ($ongkir->getStatus() == 200) {
            return json_decode($ongkir->getBody());
        } else {
            return 0;
        }
    }

    public function getCountryDestinationByCode($country_code)
    {
        $country_name = $this->getCountry($country_code);
        $curl = curl_init();

        curl_setopt_array($curl, array(
            CURLOPT_URL => "https://pro.rajaongkir.com/api/v2/internationalDestination",
            CURLOPT_RETURNTRANSFER => true,
            CURLOPT_ENCODING => "",
            CURLOPT_MAXREDIRS => 1,
            CURLOPT_TIMEOUT => 30,
            CURLOPT_HTTP_VERSION => CURL_HTTP_VERSION_1_1,
            CURLOPT_CUSTOMREQUEST => "GET",
            CURLOPT_HTTPHEADER => array(
                "key: 740f60f5764adf293b88989e737101fe"
            ),
        ));

        $response = json_decode(curl_exec($curl));
        $err = curl_error($curl);
        if (empty($err)) {
            $countries = $response->rajaongkir->results;
            $find = array_filter(
                $countries,
                function ($item) use ($country_name) {
                    return $item->country_name == $country_name;
                }
            );
            return (!empty($find) ? array_values($find)[0]->country_id : 0);
        }
        return 0;
    }

